weather = 'sunny'
case weather
when 'sunny'
  puts "Wear a short sleeved shirt to work!"
when 'rainy'
  puts "Bring an umbrella."
when 'foggy'
  puts "Drive a lot slower today."
when 'snowing'
  puts "Prepare for TERRIBLE drivers!"
when 'tornado'
  puts "Wear your ruby slippers."
when 'sharknado'
  puts "Be sure to avoid coastal cities."
else
  puts "Open your shades and take a look!"
end
